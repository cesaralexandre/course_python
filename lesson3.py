# Single Quptes
print('César Alexandre')

# Double Quotes
print("César Alexandre")

# Escape
print('César \'Alexandre\'')

# r
print(r'César \'Alexandre\'')

# An ideal way to include quotes within a string is to use both single and double quotes.
print('César "Alexandre"')